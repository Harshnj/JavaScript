let arr=[7,9,7,5,5,1,3,7,0,7];

arr.push(10);  //add element at last index
arr.pop(10);   //removes element at last index
arr.unshift(10); //add element at first index
arr.shift(10);   //removes element at first index

arr.sort();//Sort in ASC by default
arr.sort().reverse();//Sort in DSC
arr.reverse();
